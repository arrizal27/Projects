package com.me.nusantara.model;

public class Pulau {
    private int photo;
    private String nama;
    private String detail;
    private String provinsi;
    private String kota_besar;
    private String luas;

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getKota_besar() {
        return kota_besar;
    }

    public void setKota_besar(String kota_besar) {
        this.kota_besar = kota_besar;
    }

    public String getLuas() {
        return luas;
    }

    public void setLuas(String luas) {
        this.luas = luas;
    }
}
